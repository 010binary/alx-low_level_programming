#include <stdlib.h>
#include <stdio.h>

/**
 * op_add - function to add 2 number
 * @a: first number
 * @b: second number
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract 2 number
 * @a: first number
 * @b: second number
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply 2 number
 * @a: first number
 * @b: second number
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide 2 number
 * @a: first number
 * @b: second number
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to get modulus of 2 number
 * @a: first number
 * @b: second number
 * Return: the modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
